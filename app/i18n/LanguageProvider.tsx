const localeMap: Record<Language, string> = {
  tr: "tr-TR",
  en: "en-GB",
  de: "de-DE",
  fr: "fr-FR"
}

type LanguageContextValue = {
  lang: Language
  locale: string
  setLang: (lang: Language) => void
  t: (key: string, vars?: Record<string, string | number>) => string
}

const LanguageContext = createContext<LanguageContextValue | null>(null)

const getInitialLanguage = (): Language => {
  if (typeof window === "undefined") return "tr"
  const stored = window.localStorage.getItem("hearme-lang") as Language | null
  if (stored && ["tr", "en", "de", "fr"].includes(stored)) return stored
  const browserLang = navigator.language.toLowerCase()
  if (browserLang.startsWith("de")) return "de"
  if (browserLang.startsWith("fr")) return "fr"
  if (browserLang.startsWith("en")) return "en"
  return "tr"
}

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Language>("tr")

  useEffect(() => {
    const initial = getInitialLanguage()
    setLangState(initial)
  }, [])

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem("hearme-lang", lang)
      document.documentElement.lang = lang
    }
  }, [lang])

  const setLang = (next: Language) => {
    setLangState(next)
  }

  const t = useMemo(() => {
    return (key: string, vars?: Record<string, string | number>) => {
      let text = translations[lang]?.[key] ?? translations.tr[key] ?? key
      if (vars) {
        Object.entries(vars).forEach(([name, value]) => {
          text = text.replace(new RegExp(`{${name}}`, "g"), String(value))
        })
      }
      return text
    }
  }, [lang])

  const value = useMemo<LanguageContextValue>(
    () => ({ lang, locale: localeMap[lang], setLang, t }),
    [lang, t]
  )

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider")
  }
  return context
}
